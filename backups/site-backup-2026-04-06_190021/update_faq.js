const postsDir = path.join(__dirname, 'src', 'content', 'posts');
const files = fs.readdirSync(postsDir).filter(f => f.startsWith('2026-04-05-') && f.endsWith('.md'));

files.forEach(filename => {
  const filePath = path.join(postsDir, filename);
  let content = fs.readFileSync(filePath, 'utf8');

  // 카테고리 추출
  const catMatch = content.match(/^category:\s*(.+)$/m);
  if (!catMatch) { console.log('카테고리 없음: ' + filename); return; }
  const category = catMatch[1].trim();

  const faqs = faqPool[category] || faqPool['생활'];

  // 기존 '자주 묻는 질문' 섹션 교체
  const oldFaqSection = /## 💬 자주 묻는 질문[\s\S]*?(?=## 🏆|$)/;
  const newFaqSection =
    '## 💬 자주 묻는 질문\n' +
    faqs.map((faq, i) =>
      `**Q${i + 1}. ${faq.q}**\n▶ ${faq.a}`
    ).join('\n\n') +
    '\n\n';

  if (oldFaqSection.test(content)) {
    content = content.replace(oldFaqSection, newFaqSection);
    fs.writeFileSync(filePath, content, 'utf8');
    console.log('FAQ 업데이트: ' + filename);
  } else {
    console.log('FAQ 섹션 없음: ' + filename);
  }
});

console.log('\n완료!');
